/*
 * Collision Dection
 */
#include "CSCIx229.h"

bool cdetection(double carX, double carZ)
{
    bool findCollision = false;
    double coordinatesX[] = {+11, -4.5, +11+11, -4.5-11, +11+22, -4.5-22, -4.5-33, +11+33,
        +11,-4.5,+11+11,-4.5-11,+11+22,-4.5-22,+11+33,-4.5-33,
        +11,-4.5,+11+11,-4.5-11,+11+22,-4.5-22,-4.5-33,+11+33,
        +11,-4.5,+11+11,-4.5-11,+11+22,-4.5-22,+11+33,-4.5-33,
        +11,-4.5,+11+11,-4.5-11,+11+22,-4.5-22,-4.5-33,+11+33,
        +11,-4.5,+11+11,-4.5-11,+11+22,-4.5-22,+11+33,-4.5-33,
        9.5,9.5,9.5,9.5,9.5,9.5,
        9.5+50,9.5+50,9.5+50,9.5+50,9.5+50,9.5+50,
        9.5-50,9.5-50,9.5-50,9.5-50,9.5-50,9.5-50,
        -3,-3,-3,-3,-3,-3,
        -3+50,-3+50,-3+50,-3+50,-3+50,-3+50,
        -3-50,-3-50,-3-50,-3-50,-3-50,-3-50,
    };
    double coordinatesZ[] = {-3, -3, -3, -3, -3, -3, -3, -3,
        10,10,10,10,10,10,10,10,
        -3+50,-3+50,-3+50,-3+50,-3+50,-3+50,-3+50,-3+50,
        10+50,10+50,10+50,10+50,10+50,10+50,10+50,10+50,
        -3-50,-3-50,-3-50,-3-50,-3-50,-3-50,-3-50,-3-50,
        10-50,10-50,10-50,10-50,10-50,10-50,10-50,10-50,
        18,18+10,18+20,18-50,18+10-50,18+20-50,
        18,18+10,18+20,18-50,18+10-50,18+20-50,
        18,18+10,18+20,18-50,18+10-50,18+20-50,
        18,18+10,18+20,18-50,18+10-50,18+20-50,
        18,18+10,18+20,18-50,18+10-50,18+20-50,
        18,18+10,18+20,18-50,18+10-50,18+20-50,
    };
    int n = sizeof(coordinatesX)/sizeof(coordinatesX[0]);
    for (int i=0; i<n; i++)
    {
        if (sqrt(pow(fabs(carX - coordinatesX[i]),2) + pow(fabs(carZ - coordinatesZ[i]),2)) < 4.0)
        {
            findCollision = true;
            break;
        }
    }
    if (carX > 95 || carX < -95 || carZ > 95 || carZ < -95)
    {
        return true;
    }
    if (findCollision == true){
        return true;
    }
    else{
        return false;
    }
    
}
